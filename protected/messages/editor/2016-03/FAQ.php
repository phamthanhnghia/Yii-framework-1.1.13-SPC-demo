                              According to the Code of Ethics, employees can accept or offer gifts and treats that are deemed acceptable according to conventional norms. Please elaborate on these in detail.
                            </a>
                          </h4>
                        </div>
                        <div id="collapseFAQ9" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingFAQ9">
                            <div class="panel-body">
                                <p>Q. According to the Code of Ethics, employees can accept or offer gifts and treats that are reasonable and deemed acceptable according to conventional norms and practices. Tell me more about what is reasonable and acceptable.</p>
                                <div class='faq_answer'>
                                <p>A. What is reasonable and acceptable refers to what is generally deemed acceptable by most people and without there being any sense of largesse. So in practical terms it may refer to a gift or treat about which the provider and recipient feel no psychological or financial burden, but which rather upholds the principle of trust and faith. However, in practical situations, it is not easy to make a clear distinction regarding whether or not the gift or treat you are receiving or offering is socially acceptable or permitted according to conventional norms. The following guidelines are prepared to help you make a proper judgment about this. In general, when all of the following guidelines are satisfied, then the gift or treat can be accepted.</p>
                                <p>1. They generally conform with accepted business practices and are taken as a sign of goodwill</p>
                                <p>2. They are not worth very much in monetary terms and are not frequently offered</p>
                                <p>3. They do not affect the reputation of the organization and its members even when the acceptance or offering of them is public</p>
                                <p>4. They are no quid pro quos involved</p>
                                </div>
                            </div>
