                            </a>
                          </h4>
                        </div>
                        <div id="collapseFAQ8" class="panel-collapse collapse in" role="tabpanel" aria-labelledby="headingFAQ8">
                          <div class="panel-body">
                              <p>Q. Sometimes employees are allowed to offer or receive gifts and treats. Please advise on the scope of stakeholders to which guidelines regarding the acceptance and offering of gift and treats are applicable to.</p>
                              <p class='faq_answer'>A. The scope include companies and individuals who trade products and services with us, or any other related government or public agencies and organizations. Also your colleagues, boss, manager, juniors are included.</p>
