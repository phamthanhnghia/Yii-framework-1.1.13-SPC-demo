                          <div class="panel-body">
                              <p>Q. Recently the company has put more emphasis on transparent management. However, sometimes the difference between the protection of our business secrets and the transparency of management becomes blurred in terms of relationships with partners or contractors. Please advise.</p>
                              <p class='faq_answer'>A. Management and technology information which are acquired and utilized for business purposes shall be protected with care. Most of such information relates to business secrets which should also be treated with special care. If you are having difficulty deciding whether to disclose internal information to improve relationships with partners or to maintain secrecy, please consult the relevant department or team regarding the confidentiality of the information in question. In any case, you don't have to disclose any business information to others if not in a business situation.</p>
                          </div>
                        </div>
