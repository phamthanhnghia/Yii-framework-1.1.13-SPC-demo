                                <p class='faq_answer'>A. If an employee behaves against the Code of Ethics as a result of an order from his/her manager, he/she will be punished with the manager in relation to what they did. 
                                <br>So if you think the order from your manager is not appropriate or unfair, please refuse flatly or request help from upper management or the Ethical Management Office.</p>
