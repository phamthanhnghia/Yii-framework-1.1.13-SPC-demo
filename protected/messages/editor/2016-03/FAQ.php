                              <p class='faq_answer'>A. Even if you are in a desperate situation, borrowing money from any person in a business relation with the company is prohibited. You may think this case does not go against the principle of trust and faith, but your acquaintance may feel obliged to accede to your request because of the business relation between you and her/him. </p>
